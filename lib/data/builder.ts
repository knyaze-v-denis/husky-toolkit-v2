export function getBuildType(score: number): BuildType {
  if (score <= 0) return 'min';
  if (score <= 3) return 'short';
  if (score <= 7) return 'std';
  return 'ext';
}

export function getEBSize(score: number): EBSizeResult {
  if (score <= 6)  return { size: 'SM', cls: 'size-sm', time: 'до 1 недели',  range: '0–6 баллов'   };
  if (score <= 12) return { size: 'MD', cls: 'size-md', time: 'до 2 недель',  range: '7–12 баллов'  };
  return               { size: 'LG', cls: 'size-lg', time: 'до 4 недель',  range: '13–18 баллов' };
}

export function fmtScore(n: number): string {
  return n % 1 === 0 ? String(n) : n.toFixed(1);
}